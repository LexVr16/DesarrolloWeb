    private void btn_crear1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crear1ActionPerformed

        Articulo articulo1 = new Articulo();

        articulo1.setIdCategoria(Integer.parseInt(txt_idCategoria.getText()));
        articulo1.setDescripcion(txt_descripcion.getText());
        articulo1.setPrecio_venta(Double.parseDouble(txt_precio_venta.getText()));
        articulo1.setStock(Integer.parseInt(txt_stock.getText()));
        articulo1.setEstado(Byte.parseByte(cbx_estado.getSelectedItem().toString()));

        articuloDao.insert(articulo1);

        ListaArticuloController.loadJTable_ListaArticulo(tabla_articulo);
    }//GEN-LAST:event_btn_crear1ActionPerformed

    private void btn_actualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizar1ActionPerformed
        Articulo newArticulo = new Articulo();

        newArticulo.setIdArticulo(Integer.parseInt(txt_idArticulo.getText()));
        newArticulo.setIdCategoria(Integer.parseInt(txt_idCategoria.getText()));
        newArticulo.setDescripcion(txt_descripcion.getText());
        newArticulo.setPrecio_venta(Double.parseDouble(txt_precio_venta.getText()));
        newArticulo.setStock(Integer.parseInt(txt_stock.getText()));

        newArticulo.setEstado(Byte.parseByte(cbx_estado.getSelectedItem().toString()));

        articuloDao.update(newArticulo);

        ListaArticuloController.loadJTable_ListaArticulo(tabla_articulo);
    }//GEN-LAST:event_btn_actualizar1ActionPerformed

