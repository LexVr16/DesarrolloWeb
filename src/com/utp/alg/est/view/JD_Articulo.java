    private void tabla_articuloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_articuloMouseClicked
        txt_idArticulo.setText(tabla_articulo.getValueAt(tabla_articulo.getSelectedRow(), 0).toString());
        txt_idCategoria.setText(tabla_articulo.getValueAt(tabla_articulo.getSelectedRow(), 1).toString());
        txt_descripcion.setText(tabla_articulo.getValueAt(tabla_articulo.getSelectedRow(), 2).toString());
        txt_precio_venta.setText(tabla_articulo.getValueAt(tabla_articulo.getSelectedRow(), 3).toString());
        txt_stock.setText(tabla_articulo.getValueAt(tabla_articulo.getSelectedRow(), 4).toString());

        int indice;
        indice = Integer.parseInt(tabla_articulo.getValueAt(tabla_articulo.getSelectedRow(), 5).toString()) + 1;

        cbx_estado.setSelectedIndex(indice);
    }//GEN-LAST:event_tabla_articuloMouseClicked

